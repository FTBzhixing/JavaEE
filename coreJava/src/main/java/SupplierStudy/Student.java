package SupplierStudy;

public class Student {
    private String name = "fangTengBo";
    private int age = 20;

    public Student() {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
